_entry:
       	mfc0    $k0, $12
       	andi    $k0, $k0, 0x0002
       	beq     $k0, $0,  exit
       	nop
       _save_context:
       	ori 	$k0, 	$0, 0x1000     # 在栈上找一块空间保存现场
           	addi 	$k0, 	$k0, -256
           	sw 	$sp, 	116($k0)        # 最先保存栈指针
           	add 	$sp, 	$k0 $0
           	sw  	$8, 32($sp)
           	sw      $9, 36($sp)
           	sw      $31, 124($sp)
       	mfhi 	$k0
           	mflo 	$k1
           	sw $k0, 128($sp)
           	sw $k1, 132($sp)
       	jal	_main_handler
       	nop

       _main_handler:
       	mfc0 	$k0, $13
       	lui     $k1  0x8000
       	and     $t0  $k0 $k1
       	bne     $t0  $0  BD
       	nop
       	jal  BD_end
       	BD:
       	mfc0 $k1 $14
       	addi $k1 $k0 4
       	mtc0 $k1 $14
       	BD_end:
       	ori 	$k1, $0, 0x007c
       	and	$k0, $k1, $k0
       	beq 	$0, $k0, interrupt
       	nop
       	ori 	$k1, $0, 16
       	beq 	$k1, $k0, Adel
       	nop
       	jal 	Adel_end
       	nop
       	Adel:
       		mfc0    $k1 	$14
       		ori $t0 $0 0x3000
       		ori $t1  $0 1
       		slt $t0 $k1 $t0
       		beq $t0 $t1 hand
       		nop
       		ori $t0  $0 0x6ffc
       		slt $t0 $t0 $k1
       		beq $t0 $t1 hand
       		nop
       		andi $k1 $k1 0xfffc
       		mtc0    $k1  	$14
       		jal Adel_end
       		hand:
       		ori     $t0 	$0 	0x3ffc
       		divu    $k1 	$t0
       		mfhi    $k1
       		andi    $k1  	$k1 	0xfffc
       		addi    $k1  	$k1 	0x3000
       		mtc0    $k1  	$14
       	Adel_end:
       		mfc0	$k0, $14
       		add	$k0, $k0, 4
       		mtc0	$k0, $14
       		jal _restore_context
       		nop

       interrupt:
       	mfc0 $k0 $13
       	andi $k0 $k0 0x1000
       	beq $k0 $0 interrupt1
       	nop
       	sb $0, 0x7f20($0)
       	jal interrupt_end
       	interrupt1:
       	andi $k0 $k0 0x0800
       	beq $k0 $0 interrupt2
       	nop
       	sw $0, 0x7f10($0)
       	jal interrupt_end
       	interrupt2:
       	andi $k0 $k0 0x0400
       	beq $k0 $0 interrupt_end
       	nop
       	sw $0, 0x7f00($0)
       	jal interrupt_end
       	interrupt_end:
       	jal _restore_context

       _restore_context:
           	lw  	$8, 32($sp)
           	lw  	$9, 36($sp)
       	lw 	$k0, 128($sp)
       	lw  	$k1, 132($sp)
       	lw      $31, 124($sp)
       	mthi 	$k0
          	mtlo 	$k1
           	lw 	$sp, 116($sp)
           	beq $0 $0 _restore
       	nop
       _restore:
       	eret
       exit:
